    public void setHeaderNavigation(){
        NavigationView naviView = (NavigationView) findViewById(R.id.nav_view);
        naviView.setNavigationItemSelectedListener(this);
        View headerView = naviView.inflateHeaderView(R.layout.nav_header_info_customer);

        TextView tvNameNavi = (TextView)headerView.findViewById(R.id.navigation_header_name);
        TextView tvEmailNavi = (TextView)headerView.findViewById(R.id.navigation_header_email);

        tvNameNavi.setText(Hawk.get("name").toString());
        tvEmailNavi.setText(Hawk.get("email").toString());
    }
